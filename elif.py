n=int(input("enter the mark:"))
if(n>=90):
    print(+n ," Grade:A+")
elif(n>=80):
    print(+n , "Grade:A")
elif(n>=70):
    print(+n , "Grade:B+")
elif(n>=60):
    print(+n , "Grade:B")
elif(n>=50):
    print(+n , "Grade:C+")
elif(n>=40):
    print(+n , "Grade:C")
else:
    print("Student is failure")